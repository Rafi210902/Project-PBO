    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        String sql = "insert into stok_barang values(?,?,?,?,?)";
        if (txt_Npembeli.getText().isEmpty() || txt_merek.getText().isEmpty() || txt_Hbeli.getText().isEmpty() || txt_Hjual.getText().isEmpty() || txt_jumlah.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data anda belum lengkap\nSilahkan melengkapinya terlebih dahulu");
        } else {
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txt_Npembeli.getText());
                stat.setString(2, txt_merek.getText());
                stat.setString(3, txt_Hbeli.getText());
                stat.setString(4, txt_Hjual.getText());
                stat.setInt(5, Integer.parseInt(txt_jumlah.getText()));
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }
            reset();
            txt_Npembeli.requestFocus();
            data_barang();
        }
    }//GEN-LAST:event_btn_insertActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        if (data_barang.getSelectedRow() == -1) {
            if (data_barang.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Data Barang Kosong");
            } else {
                JOptionPane.showMessageDialog(null, "Pilih Data yang akan diubah");
            }
        } else {
            String sql = "update stok_barang set merek = '" + txt_merek.getText() + "',harga_jual = '" + txt_Hjual.getText() + "',harga_beli ='" + txt_Hbeli.getText() + "',jumlah ='" + txt_jumlah.getText() + "' WHERE nama_pembeli ='" + txt_Npembeli.getText() + "'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil di Ubah");
                reset();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Perubahan Data Gagal");
            }
            data_barang();
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        String sqlbarang = "delete from stok_barang where nama_pembeli = '" + data_barang.getValueAt(data_barang.getSelectedRow(), 0).toString() + "'";
        if (data_barang.getSelectedRow() == -1) {
            if (data_barang.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Tidak ada data dalam tabel");
            } else {
                JOptionPane.showMessageDialog(null, "Pilih Data Yang akan dihapus");
            }
        } else {
            try {
                PreparedStatement stat = conn.prepareStatement(sqlbarang);
                stat.execute();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Data tidak berhasil dihapus");
            }
            data_barang();
            reset();
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

