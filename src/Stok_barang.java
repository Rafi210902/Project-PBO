    void data_barang () {
        String[] Data = {"Nama_Pembeli", "merek", "Harga Beli", "Harga Jual", "Jumlah"};
        tabmodel = new DefaultTableModel(null, Data);
        data_barang.setModel(tabmodel);
        String sql = "select' from data_barang";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String Nama_Pembeli = hasil.getString("Nama_Pembeli");
                String merek = hasil.getString("merek");
                String harga_beli = hasil.getString("Harga Beli");
                String harga_jual = hasil.getString("Harga Jual");
                String jumlah = hasil.getString("Jumlah");
                String[] data = {Nama_Pembeli,merek,harga_beli,harga_jual,jumlah};
                tabmodel.addRow(data); 
            }
        } catch (SQLException ex) {
            Logger.getLogger(Normalizer.Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
